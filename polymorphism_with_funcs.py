def add(a, b):
    return a + b

# Works with integers
print(add(5, 10))  # Output: 15

# Works with strings
print(add("Hello, ", "World!"))  # Output: Hello, World!

# Works with lists
print(add([1, 2], [3, 4]))  # Output: [1, 2, 3, 4]
