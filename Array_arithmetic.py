import numpy as np
# Array arithmetic
a = np.array([1, 2, 3])
b = np.array([4, 5, 6])

# Element-wise addition, subtraction, multiplication, division
print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)

# Array statistics
print("Mean:", np.mean(a))
print("Sum:", np.sum(a))
print("Max:", np.max(a))
